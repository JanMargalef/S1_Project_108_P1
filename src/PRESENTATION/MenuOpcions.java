package PRESENTATION;

/**
 * Enumeració que representa les opcions disponibles al menú principal.
 * Les opcions inclouen:
 * - MENU_PRINCIPAL: Tornar al menú principal.
 * - GESTIO_PRODUCTES: Accedir al menú de gestió de productes.
 * - GESTIO_TENDES: Accedir al menú de gestió de tendes.
 * - BUSCAR_PRODUCTES: Buscar productes.
 * - LLISTAR_TENDES: Llistar les tendes disponibles.
 * - CARRITO: Accedir al carret de la compra.
 * - EXIT: Sortir de l'aplicació.
 */
public enum MenuOpcions {
    MENU_PRINCIPAL,
    GESTIO_PRODUCTES,
    GESTIO_TENDES,
    BUSCAR_PRODUCTES,
    LLISTAR_TENDES,
    CARRITO,
    EXIT

}
