package PRESENTATION;

/**
 * Enumeració que representa les opcions disponibles al menú de gestió de productes.
 * Les opcions inclouen:
 * - CREAR_PRODUCTE: Per a crear un nou producte.
 * - BORRAR_PRODUCTE: Per a esborrar un producte existent.
 * - BACK: Tornar enrere al menú anterior.
 * - ERROR: Opció per a gestionar errors.
 */
public enum MenuGestioProductes {
    CREAR_PRODUCTE,
    BORRAR_PRODUCTE,
    BACK,
    ERROR

}
