package PRESENTATION;

/**
 * Enumeració que representa les opcions disponibles al menú de gestió de tendes.
 * Les opcions inclouen:
 * - CREAR_TENDA: Per a crear una nova tenda.
 * - EXPANDIR_CATALEG: Per a expandir el catàleg de la tenda.
 * - REDUIR_CATALEG: Per a reduir el catàleg de la tenda.
 * - BACK: Tornar enrere al menú anterior.
 */
public enum MenuGestioTendes {

    CREAR_TENDA,
    EXPANDIR_CATALEG,
    REDUIR_CATALEG,
    BACK
}
